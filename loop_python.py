# loop in python

# dic
sami = {
    'case#1':'Died',
    'case#2':'return from life',
    'case#3':'still a life some where'
}

# print values only
for i in sami.values():
    print(i)


# print keys
for i in sami.keys():
    print(i)

# tuple
blade = (1,'Yes', 'Ok', 'Jack')

for shiny in blade:
    print(shiny)

# list
blade2 = [1,'Yes', 'Ok', 'Jack']

for shin in blade2:
    print(shin)
